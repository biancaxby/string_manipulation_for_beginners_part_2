# Prog01. lstrip() remove the space characters at the beginning of the string. Create a program that do the same functionality without using lstrip() function.

# Ask user for input
print('This program will mimic the lstrip() funtion without using lstrip()')
sentence = input('Enter something: ')
# Split the statement 
my_list = []
split_sentence = sentence.split
append_list = my_list.append(split_sentence)

# Join or combine the statement
join_sentence = ' '.join(append_list)
print(join_sentence)

